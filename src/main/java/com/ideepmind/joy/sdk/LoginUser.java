package com.ideepmind.joy.sdk;

import java.io.Serializable;

/**
 * @author tby
 * 登录用户信息
 */
public class LoginUser implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * SessionKey
	 */
	private String sessionKey;
	
	private String accessToken;
	
	private String sessionId;
	
	private String dataKey;
	
	private boolean isTemp;
	
	/**
	 * 语音视频通话用户信息
	 */
	private ContactIMInfo contactIMInfo;

	/**
	 * @return the sessionKey
	 */
	public String getSessionKey() {
		return sessionKey;
	}

	/**
	 * @param sessionKey the sessionKey to set
	 */
	public void setSessionKey(String sessionKey) {
		this.sessionKey = sessionKey;
	}

	/**
	 * @return the contactIMInfo
	 */
	public ContactIMInfo getContactIMInfo() {
		return contactIMInfo;
	}

	/**
	 * @param contactIMInfo the contactIMInfo to set
	 */
	public void setContactIMInfo(ContactIMInfo contactIMInfo) {
		this.contactIMInfo = contactIMInfo;
	}

	/**
	 * @return the 是否临时账号
	 */
	public boolean isTemp() {
		return isTemp;
	}

	/**
	 * @param 设置是否临时账号
	 */
	public void setTemp(boolean isTemp) {
		this.isTemp = isTemp;
	}

	public String getAccessToken() {
		return accessToken;
	}

	public void setAccessToken(String accessToken) {
		this.accessToken = accessToken;
	}

	public String getSessionId() {
		return sessionId;
	}

	public void setSessionId(String sessionId) {
		this.sessionId = sessionId;
	}

	public String getDataKey() {
		return dataKey;
	}

	public void setDataKey(String dataKey) {
		this.dataKey = dataKey;
	}
}
